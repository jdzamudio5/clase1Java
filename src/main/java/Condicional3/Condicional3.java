package Condicional3;

public class Condicional3 {
    public static void main(String[] args) {
        int numero =7;
        System.out.println("Tabla del 7:");
        for(int i=1; i<=10; i++){
            int res = numero* i;
            System.out.println(numero+" X "+ i+" = "+ res);
        }
    }
}
